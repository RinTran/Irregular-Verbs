package com.rintran.irregularverbs;



public class Item {

    private String name;
    private int image;

    public Item() {
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setImage(int image) {
        this.image = image;
    }

}
